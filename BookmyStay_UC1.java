/**
 * Hotel Booking System Application
 * This class represents the entry point of the Book My Stay application.
 * It demonstrates how a Java program begins execution and prints a welcome message.
 *
 * @author Student
 * @version 1.0
 */
public class BookmyStay_UC1 {

    /**
     * Main method - entry point of the Java application.
     * The JVM starts execution from this method.
     *
     * @param args command line arguments
     */
    public static void main(String[] args) {

        // Display welcome message
        System.out.println("=================================");
        System.out.println(" Welcome to Book My Stay App ");
        System.out.println(" Hotel Booking System v1.0 ");
        System.out.println("=================================");

        // Inform user application started successfully
        System.out.println("Application started successfully.");

        // Inform user application is terminating
        System.out.println("Thank you for using Book My Stay!");
    }
}